/**
 * Default template path
 */
const DEFAULT_TEMPLATE_PATH = path.join(__dirname, '../../docs/templates/prd_template.md');

/**
 * Load PRD template from disk
 */
async function loadTemplate(customPath?: string): Promise<string> {
  const templatePath = customPath || DEFAULT_TEMPLATE_PATH;

  try {
    const content = await fs.readFile(templatePath, 'utf-8');
    return content;
  } catch (error) {
    throw new Error(
      `Failed to load PRD template from ${templatePath}: ${error instanceof Error ? error.message : 'Unknown error'}`,
      { cause: error }
    );
  }
}

/**
 * Substitute template variables with values
 */
function substituteVariables(template: string, variables: Record<string, string>): string {
  let result = template;

  for (const [key, value] of Object.entries(variables)) {
    const placeholder = `{{${key}}}`;
    result = result.split(placeholder).join(value);
  }

  return result;
}

/**
 * Format context citations as markdown
 */
function formatContextCitations(contextDoc: ContextDocument): string {
  const fileEntries = Object.values(contextDoc.files);

  if (fileEntries.length === 0) {
    return '_No context files available._';
  }

  const lines: string[] = [];
  const topFiles = fileEntries
    .sort((a, b) => (b.token_count ?? 0) - (a.token_count ?? 0))
    .slice(0, 10);

  for (const file of topFiles) {
    lines.push(
      `- \`${file.path}\` (${file.token_count ?? 0} tokens, hash: \`${file.hash.substring(0, 8)}\`)`
    );
  }

  if (fileEntries.length > 10) {
    lines.push(`- _...and ${fileEntries.length - 10} more files_`);
  }

  return lines.join('\n');
}

/**
 * Format research task citations as markdown
 */
function formatResearchCitations(tasks: ResearchTask[]): string {
  if (tasks.length === 0) {
    return '_No research tasks available._';
  }

  const lines: string[] = [];
  const completedTasks = tasks.filter((t) => t.status === 'completed');

  for (const task of completedTasks) {
    const confidence = task.results?.confidence_score ?? 0;
    const sourcesCount = task.results?.sources_consulted.length ?? 0;
    lines.push(
      `- **${task.title}** (Task ID: \`${task.task_id}\`, Confidence: ${(confidence * 100).toFixed(0)}%, Sources: ${sourcesCount})`
    );
  }

  if (completedTasks.length === 0 && tasks.length > 0) {
    lines.push('_Research tasks pending completion._');
  }

  return lines.join('\n');
}

/**
 * Generate default section content with placeholders
 */
function generateDefaultSectionContent(sectionType: PRDSectionType): string {
  const placeholders: Record<PRDSectionType, string> = {
    problem_statement:
      '_TODO: Describe the problem this feature solves. What is the current pain point? Who experiences it? What is the impact?_',
    goals:
      '_TODO: List the primary objectives this feature aims to achieve. Make goals specific, measurable, and time-bound._\n\n- Goal 1: ...\n- Goal 2: ...',
    non_goals:
      '_TODO: Explicitly state what this feature will NOT address. This helps set boundaries and manage expectations._\n\n- Non-goal 1: ...\n- Non-goal 2: ...',
    acceptance_criteria:
      '_TODO: Define how we will measure success and know when the feature is complete._\n\n- Criterion 1: ...\n- Criterion 2: ...',
    risks:
      '_TODO: Identify potential risks and mitigation strategies._\n\n- **Risk 1:** Description\n  - _Mitigation:_ Strategy\n- **Risk 2:** Description\n  - _Mitigation:_ Strategy',
    open_questions:
      '_TODO: List unresolved questions that require research or stakeholder input._\n\n- Question 1: ...\n- Question 2: ...',
  };

  return placeholders[sectionType];
}

/**
 * Create default PRD sections
 */
function createDefaultSections(): Record<PRDSectionType, PRDSection> {
  const sectionTypes: PRDSectionType[] = [
    'problem_statement',
    'goals',
    'non_goals',
    'acceptance_criteria',
    'risks',
    'open_questions',
  ];

  const sections: Partial<Record<PRDSectionType, PRDSection>> = {};

  for (const type of sectionTypes) {
    sections[type] = {
      type,
      title: getSectionTitle(type),
      content: generateDefaultSectionContent(type),
      researchCitations: [],
      contextCitations: [],
    };
  }

  return sections as Record<PRDSectionType, PRDSection>;
}

/**
 * Get human-readable section title
 */
function getSectionTitle(type: PRDSectionType): string {
  const titles: Record<PRDSectionType, string> = {
    problem_statement: 'Problem Statement',
    goals: 'Goals',
    non_goals: 'Non-Goals',
    acceptance_criteria: 'Success Criteria & Acceptance Criteria',
    risks: 'Risks & Mitigations',
    open_questions: 'Open Questions',
  };

  return titles[type];
}

/**
 * Build template variables from PRD document
 */
function buildTemplateVariables(
  prdDoc: PRDDocument,
  contextDoc: ContextDocument,
  researchTasks: ResearchTask[]
): Record<string, string> {
  const sections = prdDoc.sections;

  return {
    FEATURE_ID: prdDoc.featureId,
    TITLE: prdDoc.title,
    CREATED_AT: prdDoc.createdAt,
    UPDATED_AT: prdDoc.updatedAt,
    STATUS: prdDoc.status,
    AUTHOR: prdDoc.author,
    PROBLEM_STATEMENT: sections.problem_statement.content,
    GOALS: sections.goals.content,
    NON_GOALS: sections.non_goals.content,
    ACCEPTANCE_CRITERIA: sections.acceptance_criteria.content,
    RISKS: sections.risks.content,
    OPEN_QUESTIONS: sections.open_questions.content,
    CONTEXT_CITATIONS: formatContextCitations(contextDoc),
    RESEARCH_CITATIONS: formatResearchCitations(researchTasks),
    TRACE_ID: prdDoc.traceId ?? '_Not yet assigned_',
    SPEC_LINKS: '_Specification pending PRD approval_',
    TASK_LINKS: '_Execution tasks pending spec approval_',
    APPROVAL_STATUS: prdDoc.status === 'approved' ? 'Approved' : 'Pending Approval',
    APPROVED_BY: '_Pending_',
    APPROVAL_DATE: '_Pending_',
    APPROVAL_HASH: '_Pending_',
  };
}

