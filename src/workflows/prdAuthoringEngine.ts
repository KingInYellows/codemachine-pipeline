/**
 * Draft a new PRD document
 */
export async function draftPRD(
  config: PRDAuthoringConfig,
  logger: StructuredLogger,
  metrics: MetricsCollector
): Promise<PRDAuthoringResult> {
  logger.info('Starting PRD authoring', {
    featureId: config.feature.feature_id,
    contextFiles: Object.keys(config.contextDocument.files).length,
    researchTasks: config.researchTasks.length,
  });

  const now = new Date().toISOString();
  const traceId = `TRACE-${Date.now()}-${Math.random().toString(36).substring(2, 9)}`;

  // Step 1: Create structured PRD document
  const sections = createDefaultSections();

  const prdDocument: PRDDocument = {
    featureId: config.feature.feature_id,
    title: config.feature.title ?? 'Untitled Feature',
    author: 'codemachine-pipeline',
    createdAt: now,
    updatedAt: now,
    status: 'draft',
    version: '1.0.0',
    traceId,
    sections,
    metadata: {
      contextDocumentHash: config.contextDocument.metadata?.manifest_hash,
      researchTaskIds: config.researchTasks.map((t) => t.task_id),
    },
  };

  // Step 2: Load and render template
  const template = await loadTemplate(config.templatePath);
  const variables = buildTemplateVariables(
    prdDocument,
    config.contextDocument,
    config.researchTasks
  );
  const renderedMarkdown = substituteVariables(template, variables);

  // Step 3: Persist PRD to run directory
  const artifactsDir = getSubdirectoryPath(config.runDir, 'artifacts');
  await fs.mkdir(artifactsDir, { recursive: true });

  const prdPath = path.join(artifactsDir, 'prd.md');
  await withLock(config.runDir, async () => {
    await fs.writeFile(prdPath, renderedMarkdown, 'utf-8');
  });

  // Step 4: Compute hash
  const prdHash = await computeFileHash(prdPath);

  // Step 5: Save metadata
  const metadata = {
    featureId: config.feature.feature_id,
    prdHash,
    createdAt: now,
    updatedAt: now,
    approvalStatus: 'pending' as const,
    approvals: [] as string[],
    version: '1.0.0',
    traceId,
  };

  const metadataPath = path.join(artifactsDir, 'prd_metadata.json');
  await withLock(config.runDir, async () => {
    await fs.writeFile(metadataPath, JSON.stringify(metadata, null, 2), 'utf-8');
  });

  // Step 6: Detect incomplete sections
  const incompleteSections: PRDSectionType[] = [];
  for (const [key, section] of Object.entries(sections)) {
    if (section.content.includes('_TODO:')) {
      incompleteSections.push(key as PRDSectionType);
    }
  }

  // Step 7: Count citations
  const totalCitations =
    Object.keys(config.contextDocument.files).length + config.researchTasks.length;

  logger.info('PRD draft completed', {
    featureId: config.feature.feature_id,
    prdPath,
    prdHash,
    incompleteSections: incompleteSections.length,
  });

  metrics.increment('prd_drafts_generated_total', {
    feature_id: config.feature.feature_id,
  });

  const warnings: string[] = [];
  if (incompleteSections.length > 0) {
    warnings.push(
      `${incompleteSections.length} section(s) contain TODO markers: ${incompleteSections.join(', ')}`
    );
  }

  if (config.researchTasks.some((t) => t.status !== 'completed')) {
    warnings.push('Some research tasks are not yet completed');
  }

  return {
    prdDocument,
    prdPath,
    prdHash,
    metadataPath,
    diagnostics: {
      usedAgent: false,
      incompleteSections,
      totalCitations,
      warnings,
    },
  };
}
