/**
 * PRD section identifiers
 */
export type PRDSectionType =
  | 'problem_statement'
  | 'goals'
  | 'non_goals'
  | 'acceptance_criteria'
  | 'risks'
  | 'open_questions';

/**
 * Individual PRD section with content and metadata
 */
export interface PRDSection {
  /** Section type identifier */
  type: PRDSectionType;
  /** Section title */
  title: string;
  /** Markdown content */
  content: string;
  /** Research task IDs cited in this section */
  researchCitations: string[];
  /** Context file paths cited in this section */
  contextCitations: string[];
  /** Confidence score (0-1) if generated by agent */
  confidence?: number;
}

/**
 * Structured PRD document representation
 */
export interface PRDDocument {
  /** Feature identifier */
  featureId: string;
  /** PRD title */
  title: string;
  /** Author identifier */
  author: string;
  /** Creation timestamp */
  createdAt: string;
  /** Last update timestamp */
  updatedAt: string;
  /** Document status */
  status: 'draft' | 'in_review' | 'approved' | 'rejected';
  /** PRD version */
  version: string;
  /** Trace identifier for requirements mapping */
  traceId?: string;
  /** Structured sections */
  sections: Record<PRDSectionType, PRDSection>;
  /** Intentional: PRD document metadata varies by authoring context */
  metadata?: Record<string, unknown>;
}

/**
 * Configuration for PRD authoring
 */
export interface PRDAuthoringConfig {
  /** Repository root directory */
  repoRoot: string;
  /** Run directory path */
  runDir: string;
  /** Feature metadata */
  feature: Feature;
  /** Context document */
  contextDocument: ContextDocument;
  /** Completed research tasks */
  researchTasks: ResearchTask[];
  /** Repository configuration */
  repoConfig: RepoConfig;
  /** Custom template path (optional) */
  templatePath?: string;
  /** Enable agent-assisted drafting (optional) */
  useAgent?: boolean;
}

/**
 * Result of PRD generation
 */
export interface PRDAuthoringResult {
  /** Structured PRD document */
  prdDocument: PRDDocument;
  /** Path to generated prd.md file */
  prdPath: string;
  /** SHA-256 hash of prd.md */
  prdHash: string;
  /** Path to metadata file */
  metadataPath: string;
  /** Generation diagnostics */
  diagnostics: {
    /** Whether agent was used */
    usedAgent: boolean;
    /** Sections with TODO markers */
    incompleteSections: PRDSectionType[];
    /** Citations count */
    totalCitations: number;
    /** Warnings */
    warnings: string[];
  };
}

